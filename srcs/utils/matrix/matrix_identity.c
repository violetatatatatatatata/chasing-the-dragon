#include <ctd.h>

void	matrix_identity(t_matrix4f m)
{
	int	i;
	int	j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			if (i == j)
				m[i][j] = 1.0f;
			else
				m[i][j] = 0.0f;
			j++;
		}
		i++;
	}
}
